                  PowerPay<sup>TM</sup>  -Your Preferred Financing Solution for Clean Energy Appliances, Equipment and demand generation.
                  PowerPay<sup>TM</sup> was founded as a subsidiary brand of GIVE LLC ,  it is a provider of high-quality clean energy systems through affordable and flexible prepaid installments stretched over a period of time in Kenya and Sub-Saharan Africa . 
                 Our systems allow off-grid low-income households and businesses to access and acquire clean energy through small-scale mobile repayments. 
                 Our Goal is to to help in the attainment of The UN’s SDG 7.
                 The United Nations’ Sustainable Development Goal 7 centres around achieving access to affordable, reliable and sustainable modern energy for all by 2030. Currently, 1.2 billion people worldwide - one in every six people on the planet - do not have access to electricity, close to 600m people in Sub-Saharan Africa.
                  Based on a business-as-usual scenario, some 780 million people are projected to remain without it by 2030. Communities often face many challenges in obtaining electricity access. 
                  These range from the prohibitively high cost of a connection, to the challenges of informal housing, the impact of power theft on services and socio-political marginalization. 
                  In many cases, these obstacles are difficult to address successfully. However, recent advances in distributed renewable energy technologies mean a more affordable, faster to deploy, cleaner alternative is at hand in Africa. One that can step in where policy and utility reforms are wanting. 
